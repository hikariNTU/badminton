      <span>3. TESTING A SHUTTLE FOR SPEED</span>
    </h2>
    <ol>
      <li>
        <p>
          <span
            >To test a shuttle, a player shall use a full underhand stroke which
            makes contact with the shuttle over the back boundary line. The
            shuttle shall be hit at an upward angle and in a direction parallel
            to the side lines.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >A shuttle of correct speed will land not less than 530 mm and not
            more than 990 mm short of the other back boundary line as in Diagram
            B.</span
          >
        </p>
      </li>
    </ol>
