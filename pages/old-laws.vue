    <h2 id="1-COURT-AND-COURT-EQUIPMENT" data-id="1-COURT-AND-COURT-EQUIPMENT">
      <span>1. COURT AND COURT EQUIPMENT</span>
    </h2>
    <ol>
      <li>
        <p>
          <span
            >The court shall be a rectangle marked out with lines 40 mm wide as
            shown in Diagram A.</span
          >
        </p>
        <p>
          <span>1.1 </span
          ><em
            ><span
              >The court for Wheelchair Badminton shall be as shown in Diagrams
              D and E, respectively.</span
            ></em
          >
        </p>
        <p>
          <span>1.2 </span
          ><em
            ><span
              >For Standing Badminton classes playing half-court the court for
              singles shall be as shown in Diagram F</span
            ></em
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >The lines marking out the court shall be easily distinguishable and
            preferably be coloured white or yellow.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >All the lines shall form part of the area which they define.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >The posts shall be 1.55 metres in height from the surface of the
            court and shall remain vertical when the net is strained as provided
            in Law 1.10.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >The posts shall be placed on the doubles side lines as in Diagram A
            irrespective of whether singles or doubles is being played. The
            posts or its supports shall not extend into the court beyond the
            side lines.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >The net shall be made of fine cord of dark colour and even
            thickness with a mesh of not less than 15 mm and not more than 20
            mm.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >The net shall be 760 mm in depth and at least 6.1 metres
            wide.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >The top of the net shall be edged with a 75 mm white tape doubled
            over a cord or cable running through the tape. This tape shall rest
            upon the cord or cable.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >The cord or cable shall be stretched firmly, flush with the top of
            the posts.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >The top of the net from the surface of the court shall be 1.524
            metres at the centre of the court and 1.55 metres over the side
            lines for doubles.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >There shall be no gaps between the ends of the net and the posts.
            If necessary, the full depth ends shall be tied to the posts.</span
          >
        </p>
      </li>
    </ol>
