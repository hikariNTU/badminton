    <h2 id="2-SHUTTLE" data-id="2-SHUTTLE"><span>2. SHUTTLE</span></h2>
    <ol>
      <li>
        <p>
          <span
            >The shuttle shall be made of natural and / or synthetic materials.
            From whatever material the shuttle is made, the flight
            characteristics generally shall be similar to those produced by a
            natural feathered shuttle with a cork base covered by a thin layer
            of leather.</span
          >
        </p>
      </li>
      <li>
        <p>
          <strong><span>Feathered Shuttle</span></strong>
        </p>
        <ol>
          <li>
            <p>
              <span>The shuttle shall have 16 feathers fixed in the base.</span>
            </p>
          </li>
          <li>
            <p>
              <span
                >The feathers shall have a uniform length between 62 mm to 70 mm
                when measured from the tip to the top of the base.</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >The tips of the feathers shall lie on a circle with a diameter
                from 58 mm to 68 mm.</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >The feathers shall be fastened firmly with thread or other
                suitable material.</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >The base shall be 25 mm to 28 mm in diameter and rounded on the
                bottom.</span
              >
            </p>
          </li>
          <li>
            <p><span>The shuttle shall weigh from 4.74 to 5.50 grams.</span></p>
          </li>
        </ol>
      </li>
      <li>
        <p>
          <strong><span>Non-Feathered Shuttle</span></strong>
        </p>
        <ol>
          <li>
            <p>
              <span
                >The skirt, or simulation of feathers in synthetic materials,
                shall replace natural feathers.</span
              >
            </p>
          </li>
          <li>
            <p><span>The base shall be as described in Law 2.2.5.</span></p>
          </li>
          <li>
            <p>
              <span
                >Measurements and weight shall be as in Laws 2.2.2, 2.2.3 and
                2.2.6. However, because of the difference in the specific
                gravity and other properties of synthetic materials in
                comparison with feathers, a variation of up to 10 per cent shall
                be acceptable.</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >Subject to there being no variation in the general design,
                speed and flight of the shuttle, modifications in the above
                specifications may be made with the approval of the Member
                Association concerned, in places where atmospheric conditions
                due to either altitude or climate make the standard shuttle
                unsuitable.</span
              >
            </p>
          </li>
        </ol>
      </li>
    </ol>
