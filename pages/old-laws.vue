    <h2 id="4-RACKET" data-id="4-RACKET"><span>4. RACKET</span></h2>
    <ol>
      <li>
        <p>
          <span
            >The racket shall be a frame not exceeding 680 mm in overall length
            and 230 mm in overall width consisting of the main parts described
            in Laws 4.1.1 to 4.1.5 as illustrated in Diagram C.</span
          >
        </p>
        <ol>
          <li>
            <p>
              <span
                >The handle is the part of the racket intended to be gripped by
                a player.</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >The stringed area is the part of the racket with which it is
                intended that a player hits the shuttle.</span
              >
            </p>
          </li>
          <li>
            <p><span>The head bounds the stringed area.</span></p>
          </li>
          <li>
            <p>
              <span
                >The shaft connects the handle to the head (subject to Law
                4.1.5).</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >The throat (if present) connects the shaft to the head.</span
              >
            </p>
          </li>
        </ol>
      </li>
    </ol>
    <div class="alert alert-info">
      <p>
        <span>DIAGRAM C</span><br />
        <img src="https://i.imgur.com/dbCdCmX.png" alt="" />
      </p>
    </div>
    <ol start="2">
      <li>
        <p>
          <strong><span>The stringed area:</span></strong>
        </p>
        <ol>
          <li>
            <span
              >shall be flat and consist of a pattern of crossed strings either
              alternately interlaced or bonded where they cross. The stringing
              pattern shall be generally uniform and, in particular, not less
              dense in the centre than in any other area; and</span
            >
          </li>
          <li>
            <span
              >shall not exceed 280 mm in overall length and 220 mm in overall
              width. However, the strings may extend into an area which
              otherwise would be the throat, provided that:</span
            >
            <ol>
              <li>
                <span
                  >the width of the extended stringed area does not exceed 35
                  mm; and</span
                >
              </li>
              <li>
                <span
                  >the overall length of the stringed area does not then exceed
                  330 mm.</span
                >
              </li>
            </ol>
          </li>
        </ol>
      </li>
      <li>
        <p>
          <strong><span>The racket:</span></strong>
        </p>
        <ol>
          <li>
            <span
              >shall be free of attached objects and protrusions, other than
              those used solely and specifically to limit or prevent wear and
              tear, or vibration, or to distribute weight, or to secure the
              handle by cord to the player’s hand, and which are reasonable in
              size and placement for such purposes; and</span
            >
          </li>
          <li>
            <span
              >shall be free of any device that makes it possible for a player
              to change materially the shape of the racket.</span
            >
          </li>
        </ol>
      </li>
    </ol>
    <h2 id="5-EQUIPMENT-AND-COMPLIANCE" data-id="5-EQUIPMENT-AND-COMPLIANCE">
      <span>5. EQUIPMENT AND COMPLIANCE</span>
    </h2>
    <ol>
      <li>
        <p>
          <strong><span>Compliance</span></strong>
        </p>
        <p>
          <span
            >The Badminton World Federation shall rule on any question of
