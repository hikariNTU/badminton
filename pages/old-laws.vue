            application by any party with a bona fide interest, including any
            player, technical official, equipment manufacturer or Member
            Association or member thereof.</span
          >
        </p>
      </li>
      <li>
        <p>
          <strong><span>Additional equipment for Para-Badminton</span></strong>
        </p>
        <p>
          <span>For Para-badminton, a wheelchair or a crutch may be used.</span>
        </p>
        <ol>
          <li>
            <em
              ><span
                >A player’s body may be fixed to the wheelchair with an elastic
                belt.</span
              ></em
            >
          </li>
          <li>
            <em
              ><span
                >A wheelchair may be equipped with supporting wheels, which may
                extend beyond the main wheel.</span
              ></em
            >
          </li>
          <li>
            <em
              ><span
                >The player’s feet must be fixed to the footrest of the
                wheelchair.</span
              ></em
            >
          </li>
        </ol>
      </li>
    </ol>
    <h2 id="6-TOSS" data-id="6-TOSS"><span>6. TOSS</span></h2>
    <ol>
      <li>
        <span
          >Before play commences, a toss shall be conducted and the side winning
          the toss shall exercise the choice in either Law 6.1.1 or 6.1.2:</span
        >
        <ol>
          <li><span>to serve or receive first;</span></li>
          <li>
            <span>to start play at one end of the court or the other.</span>
          </li>
        </ol>
      </li>
      <li>
        <span
          >The side losing the toss shall then exercise the remaining
          choice.</span
        >
      </li>
    </ol>
    <h2 id="7-SCORING-SYSTEM" data-id="7-SCORING-SYSTEM">
      <span>7. SCORING SYSTEM</span>
    </h2>
    <ol>
      <li>
        <span
          >A match shall consist of the best of three games, unless otherwise
          arranged (Part II Section 1B Appendix 2 and 3).</span
        >
      </li>
      <li>
        <span
          >A game shall be won by the side which first scores 21 points, except
          as provided in Law 7.4 and 7.5.</span
        >
      </li>
      <li>
        <span
          >The side winning a rally shall add a point to its score. A side shall
          win a rally, if the opposing side commits a ‘fault’ or the shuttle
          ceases to be in play because it touches the surface of the court
          inside the opponent’s court.</span
        >
      </li>
      <li>
        <span
          >If the score becomes 20-all, the side which gains a two point lead
          first, shall win that game.</span
        >
      </li>
      <li>
        <span
          >If the score becomes 29-all, the side scoring the 30th point shall
          win that game.</span
        >
      </li>
      <li>
        <span>The side winning a game shall serve first in the next game.</span>
      </li>
    </ol>
    <h2 id="8-CHANGE-OF-ENDS" data-id="8-CHANGE-OF-ENDS">
      <span>8. CHANGE OF ENDS</span>
    </h2>
    <ol>
      <li>
        <span>Players shall change ends:</span>
        <ol>
          <li><span>at the end of the first game;</span></li>
          <li>
            <span
              >at the end of the second game, if there is to be a third game;
              and</span
            >
          </li>
          <li>
            <span>in the third game when a side first scores 11 points.</span>
          </li>
        </ol>
      </li>
      <li>
        <span
          >If the ends are not changed as indicated in Law 8.1, it shall be done
          so as soon as the mistake is discovered and when the shuttle is not in
          play. The existing score shall stand.</span
        >
      </li>
    </ol>
    <h2 id="9-SERVICE" data-id="9-SERVICE"><span>9. SERVICE</span></h2>
    <ol>
      <li>
        <p><span>In a correct service,</span></p>
        <ol>
          <li>
            <p>
              <span
                >neither side shall cause undue delay to the delivery of the
                service once the server and the receiver are ready for the
                service.</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >on completion of the backward movement of the server’s racket
                head, any delay in the start of the service (Law 9.2) shall be
                considered to be an undue delay;</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >the server and the receiver shall stand within diagonally
                opposite service courts (Diagram A) without touching the
                boundary lines of these service courts;</span
              >
            </p>
            <ol>
              <li>
                <em
                  ><span
                    >In Para-badminton Wheelchair and Standing Classes playing
                    half-court, Diagram D and F, respectively, apply.</span
                  ></em
                >
              </li>
            </ol>
          </li>
          <li>
            <p>
              <span
                >some part of both feet of the server and the receiver shall
                remain in contact with the surface of the court in a stationary
                position from the start of the service (Law 9.2) until the
                service is delivered (Law 9.3);</span
              >
            </p>
            <ol>
              <li>
                <em
                  ><span
                    >In Wheelchair Badminton: from the start of the service and
                    until the service is delivered, the wheels of the server and
                    the receiver must be stationary, except the natural counter
                    movement of the server’s wheelchair.</span
                  ></em
                >
              </li>
            </ol>
          </li>
          <li>
            <p>
              <span
                >the server’s racket shall initially hit the base of the
                shuttle;</span
              >
            </p>
          </li>
          <li>
            <p>
              <s
                ><span
                  >the whole shuttle shall be below the server’s waist at the
                  instant of being hit by the server’s racket. The waist shall
                  be considered to be an imaginary line round the body, level
                  with the lowest part of the server’s bottom rib;</span
                ></s
              ><br />
              <span
                >the whole shuttle shall be below 1.15 metres from the surface
                of the court at the instant of being hit by the server’s
                racket;</span
              >
            </p>
            <ol>
              <li>
                <p>
                  <em
                    ><span
                      >In Wheelchair Badminton, the whole shuttle shall be below
                      the server’s armpit at the instant of being hit by the
                      server’s racket;</span
                    ></em
                  >
                </p>
              </li>
              <li>
                <p>
                  <s
                    ><span
                      >For Fixed Height Experiment: The whole of the shuttle
                      shall be below 1.10 metres from the surface of the court
                      at the instant of being hit by the server’s racket.</span
                    ></s
                  >
                </p>
              </li>
            </ol>
          </li>
        </ol>
        <p>
          <s><span>7.</span></s
          ><span> </span
          ><s
            ><span
              >the shaft and the racket head of the server’s racket at the
              instant of hitting the shuttle shall be pointing in a downward
              direction;</span
            ></s
          >
        </p>
        <ol start="7">
          <li>
            <span
              >the movement of the server’s racket shall continue forwards from
              the start of the service (Law 9.2) until the service is delivered
              (Law 9.3);</span
            >
          </li>
          <li>
            <span
              >the flight of the shuttle shall be upwards from the server’s
              racket to pass over the net so that, if not intercepted, it shall
              land in the receiver’s service court (i.e. on or within the
              boundary lines); and</span
            >
          </li>
          <li>
            <span
              >in attempting to serve, the server shall not miss the
              shuttle.</span
            >
          </li>
        </ol>
      </li>
      <li>
        <p>
          <span
            >Once the players are ready for the service, the first forward
            movement of the server’s racket head shall be the start of the
            service.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >Once started (Law 9.2), the service is delivered when the shuttle
            is hit by the server’s racket or, in attempting to serve, the server
            misses the shuttle.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >The server shall not serve before the receiver is ready. However,
            the receiver shall be considered to have been ready if a return of
            the service is attempted.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >In doubles, during the delivery of service (Law 9.2, 9.3), the
            partners may take up any positions within their respective courts,
            which do not unsight the opposing server or receiver.</span
          >
        </p>
      </li>
    </ol>
    <h2 id="10-SINGLES" data-id="10-SINGLES"><span>10. SINGLES</span></h2>
    <ol>
      <li>
        <p>
          <strong><span>Serving and receiving courts</span></strong>
        </p>
        <ol>
          <li>
            <span
              >The players shall serve from, and receive in, their respective
              right service courts when the server has not scored or has scored
              an even number of points in that game.</span
            >
          </li>
          <li>
            <span
              >The players shall serve from, and receive in, their respective
              left service courts when the server has scored an odd number of
              points in that game.</span
            >
          </li>
          <li>
            <span
              >In Para-badminton Classes playing half-court, the server and
              receiver shall serve from and receive in their respective service
              courts.</span
            >
          </li>
        </ol>
      </li>
      <li>
        <p>
          <strong><span>Order of play and position on court</span></strong>
        </p>
        <ol>
          <li>
            <span
              >In a rally, the shuttle may be hit by the server and the receiver
              alternately, from any positionon that player’s side of the net,
              until the shuttle ceases to be in play (Law 15).</span
            >
          </li>
        </ol>
      </li>
      <li>
        <p>
          <strong><span>Scoring and serving</span></strong>
        </p>
        <ol>
          <li>
            <p>
              <span
                >If the server wins a rally (Law 7.3), the server shall score a
                point. The server shall then serve again from the alternate
                service court.</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >If the receiver wins a rally (Law 7.3), the receiver shall
                score a point. The receiver shall then become the new
                server.</span
              >
            </p>
          </li>
        </ol>
      </li>
    </ol>
    <h2 id="11-DOUBLES" data-id="11-DOUBLES"><span>11. DOUBLES</span></h2>
    <ol>
      <li>
        <p>
          <strong><span>Serving and receiving courts</span></strong>
        </p>
        <ol>
          <li>
            <span
              >A player of the serving side shall serve from the right service
              court when the serving side has not scored or has scored an even
              number of points in that game.</span
            >
          </li>
          <li>
            <span
              >A player of the serving side shall serve from the left service
              court when the serving side has scored an odd number of points in
              that game.</span
            >
          </li>
          <li>
            <span
              >The player of the receiving side who served last shall stay in
              the same service court from where he served last. The reverse
              pattern shall apply to the receiver’s partner.</span
            >
          </li>
          <li>
            <span
              >The player of the receiving side standing in the diagonally
              opposite service court to the server shall be the receiver.</span
            >
          </li>
          <li>
            <span
              >The players shall not change their respective service courts
              until they win a point when their side is serving.</span
            >
          </li>
          <li>
            <span
              >Service in any turn of serving shall be delivered from the
              service court corresponding to the serving side’s score, except as
              provided in Law 12.</span
            >
          </li>
        </ol>
      </li>
      <li>
        <p>
          <strong><span>Order of play and position on court</span></strong>
        </p>
        <p>
          <span
            >After the service is returned, in a rally, the shuttle may be hit
            by either player of the serving side and either player of the
            receiving side alternately, from any position on that player’s side
            of the net, until the shuttle ceases to be in play (Law 15).</span
          >
        </p>
      </li>
      <li>
        <p>
          <strong><span>Scoring and serving</span></strong>
        </p>
        <ol>
          <li>
            <p>
              <span
                >If the serving side wins a rally (Law 7.3), the serving side
                shall score a point. The server shall then serve again from the
                alternate service court.</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >If the receiving side wins a rally (Law 7.3), the receiving
                side shall score a point. The receiving side shall then become
                the new serving side.</span
              >
            </p>
          </li>
        </ol>
      </li>
      <li>
        <p>
          <strong><span>Sequence of serving</span></strong>
        </p>
        <p>
          <span>In any game, the right to serve shall pass consecutively:</span>
        </p>
        <ol>
          <li>
            <span
              >from the initial server who started the game from the right
              service court</span
            >
          </li>
          <li><span>to the partner of the initial receiver.</span></li>
          <li><span>to the partner of the initial server</span></li>
          <li><span>to the initial receiver,</span></li>
          <li><span>to the initial server and so on.</span></li>
        </ol>
      </li>
      <li>
        <p>
          <span
            >No player shall serve or receive out of turn, or receive two
            consecutive services in the same game, except as provided in Law
            12.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >Either player of the winning side may serve first in the next game,
            and either player of the losing side may receive first in the next
            game.</span
          >
        </p>
      </li>
    </ol>
    <h2 id="12-SERVICE-COURT-ERRORS" data-id="12-SERVICE-COURT-ERRORS">
      <span>12. SERVICE COURT ERRORS</span>
    </h2>
    <ol>
      <li>
        <p><span>A service court error has been made when a player:</span></p>
        <ol>
          <li><span>has served or received out of turn; or</span></li>
          <li>
            <span>has served or received from the wrong service court;</span>
          </li>
        </ol>
      </li>
      <li>
        <p>
          <span
            >If a service court error is discovered, the error shall be
            corrected and the existing score shall stand.</span
          >
        </p>
      </li>
    </ol>
    <h2 id="13-FAULTS" data-id="13-FAULTS"><span>13. FAULTS</span></h2>
    <p><span>It shall be a ‘fault’:</span></p>
    <ol>
      <li>
        <p><span>if a service is not correct (Law 9.1);</span></p>
      </li>
      <li>
        <p><span>if, in service, the shuttle:</span></p>
        <ol>
          <li>
            <span>is caught on the net and remains suspended on its top;</span>
          </li>
          <li>
            <span>after passing over the net, is caught in the net; or</span>
          </li>
          <li><span>is hit by the receiver’s partner;</span></li>
        </ol>
      </li>
      <li>
        <p><span>if in play, the shuttle:</span></p>
        <ol>
          <li>
            <span
              >lands outside the boundaries of the court (i.e. not on or within
              the boundary lines);</span
            >
          </li>
          <li><span>fails to pass over the net;</span></li>
          <li><span>touches the ceiling or side walls;</span></li>
          <li>
            <span>touches the person or dress of a player;</span>
            <ol>
              <li>
                <em
                  ><span
                    >In Para-badminton a Wheelchair or Crutch is considered part
                    of the player’s person.</span
                  ></em
                >
              </li>
            </ol>
          </li>
          <li>
            <span>touches any other object or person outside the court;</span>
          </li>
        </ol>
        <p>
          <span
            >(Where necessary on account of the structure of the building, the
            local badminton authority may, subject to the right of veto of its
            Member Association, make bye-laws dealing with cases in which a
            shuttle touches an obstruction).</span
          >
        </p>
        <ol start="6">
          <li>
            <span
              >is caught and held on the racket and then slung during the
              execution of a stroke;</span
            >
          </li>
          <li>
            <span
              >is hit twice in succession by the same player. However, a shuttle
              hitting the head and the stringedarea of the racket in one stroke
              shall not be a ‘fault’;</span
            >
          </li>
          <li>
            <span
              >is hit by a player and the player’s partner successively;
              or</span
            >
          </li>
          <li>
            <span
              >touches a player’s racket and does not travel towards the
              opponent’s court;</span
            >
          </li>
        </ol>
      </li>
      <li>
        <p><span>if, in play, a player:</span></p>
        <ol>
          <li>
            <p>
              <span
                >touches the net or its supports with racket, person or
                dress;</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >invades an opponent’s court over the net with racket or person
                except that the striker may follow the shuttle over the net with
                the racket in the course of a stroke after the initial point of
                contact with the shuttle is on the striker’s side of the
                net;</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >invades an opponent’s court under the net with racket or person
                such that an opponent is obstructed or distracted; or</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >obstructs an opponent, i.e. prevents an opponent from making a
                legal stroke where the shuttle is followed over the net;</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >deliberately distracts an opponent by any action such as
                shouting or making gestures;</span
              >
            </p>
          </li>
          <li>
            <p><span>in Wheelchair Badminton</span></p>
            <ol>
              <li>
                <em
                  ><span
                    >at the moment the shuttle is hit no part of the players’
                    trunk is in contact with the seat of the wheelchair.</span
                  ></em
                >
              </li>
              <li>
                <em
                  ><span
                    >if the fixation of a foot to the footrest is lost.</span
                  ></em
                >
              </li>
              <li>
                <em
                  ><span
                    >during play, the player touches the floor with any part of
                    the feet.</span
                  ></em
                >
              </li>
            </ol>
          </li>
        </ol>
      </li>
      <li>
        <p>
          <span
            >if a player is guilty of flagrant, repeated or persistent offences
            under Law 16.</span
          >
        </p>
      </li>
    </ol>
    <h2 id="14-LETS" data-id="14-LETS"><span>14. LETS</span></h2>
    <ol>
      <li>
        <p>
          <span
            >‘Let’ shall be called by the umpire, or by a player (if there is no
            umpire), to halt play.</span
          >
        </p>
      </li>
      <li>
        <p><span>It shall be a ‘let”, if:</span></p>
        <ol>
          <li>
            <span
              >the server serves before the receiver is ready (Law 9.4);</span
            >
          </li>
          <li>
            <span
              >during service, the receiver and the server are both
              faulted;</span
            >
          </li>
          <li>
            <span>after the service is returned, the shuttle is:</span>
            <ol>
              <li>
                <span
                  >caught on the net and remains suspended on its top, or</span
                >
              </li>
              <li>
                <span>after passing over the net is caught in the net;</span>
              </li>
            </ol>
          </li>
          <li>
            <span
              >during play, the shuttle disintegrates and the base completely
              separates from the rest of the shuttle;</span
            >
          </li>
          <li>
            <span
              >in the opinion of the umpire, play is disrupted or a player of
              the opposing side is distracted by a coach;</span
            >
          </li>
          <li>
            <span
              >a line judge is unsighted and the umpire is unable to make a
              decision; or</span
            >
          </li>
          <li>
            <span>any unforeseen or accidental situation has occurred.</span>
          </li>
        </ol>
      </li>
      <li>
        <p>
          <span
            >When a ‘let’ occurs, play since the last service shall not count
            and the player who served last shall serve again.</span
          >
        </p>
      </li>
    </ol>
    <h2 id="15-SHUTTLE-NOT-IN-PLAY" data-id="15-SHUTTLE-NOT-IN-PLAY">
      <span>15. SHUTTLE NOT IN PLAY</span>
    </h2>
    <p><span>A shuttle is not in play when:</span></p>
    <ol>
      <li>
        <p>
          <span
            >it strikes the net or post and starts to fall towards the surface
            of the court on the striker’s side of the net;</span
          >
        </p>
      </li>
      <li>
        <p><span>it hits the surface of the court; or</span></p>
      </li>
      <li>
        <p><span>a ‘fault’ or a ‘let’ has occurred.</span></p>
      </li>
    </ol>
    <h2
      id="16-CONTINUOUS-PLAY-MISCONDUCT-amp-PENALTIES"
      data-id="16-CONTINUOUS-PLAY-MISCONDUCT-amp-PENALTIES"
    >
      <span>16. CONTINUOUS PLAY, MISCONDUCT &amp; PENALTIES</span>
    </h2>
    <ol>
      <li>
        <p>
          <span
            >Play shall be continuous from the first service until the match is
            concluded, except as allowed in Laws 16.2 and 16.3, and, for
            Wheelchair Badminton, 16.5.3.</span
          >
        </p>
      </li>
      <li>
        <p>
          <strong><span>Intervals:</span></strong>
        </p>
        <ol>
          <li>
            <p>
              <span
                >not exceeding 60 seconds during each game when the leading
                score reaches 11 points; and</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >not exceeding 120 seconds between the first and second game,
                and between the second and third game shall be allowed in all
                matches.</span
              >
            </p>
          </li>
        </ol>
        <p>
          <span
            >(For a televised match, the Referee may decide before the match
            that intervals as in Law 16.2 are mandatory and of fixed
            duration).</span
          >
        </p>
      </li>
      <li>
        <p>
          <strong><span>Suspension of play</span></strong>
        </p>
        <ol>
          <li>
            <span
              >When necessitated by circumstances not within the control of the
              players, the umpire may suspend play for such a period as the
              umpire may consider necessary.</span
            >
          </li>
          <li>
            <span
              >Under special circumstances the Referee may instruct the umpire
              to suspend play. In Para-badminton, repair of additional equipment
              for Para-badminton (Law 5.2) may be considered a special
              circumstance.</span
            >
          </li>
          <li>
            <span
              >If play is suspended, the existing score shall stand and play
              shall be resumed from that point.</span
            >
          </li>
        </ol>
      </li>
      <li>
        <p>
          <strong><span>Delay in play</span></strong>
        </p>
        <ol>
          <li>
            <span
              >Under no circumstances shall play be delayed to enable a player
              to recover strength or wind or to receive advice.</span
            >
          </li>
          <li>
            <span
              >The umpire shall be the sole judge of any delay in play.</span
            >
          </li>
        </ol>
      </li>
      <li>
        <p>
          <strong><span>Advice and leaving the court</span></strong>
        </p>
        <ol>
          <li>
            <span
              >Only when the shuttle is not in play (Law 15), shall a player be
              permitted to receive advice during a match.</span
            >
          </li>
          <li>
            <span
              >No player shall leave the court during a match without the
              umpire’s permission, except during the intervals as described in
              Law 16.2.</span
            >
          </li>
          <li>
            <span
              >In Wheelchair Badminton, a player may be allowed to leave the
              court for one additional interval during a match in order to
              catheterise. The player shall be accompanied by any BWF appointed
              Technical Official.</span
            >
          </li>
        </ol>
      </li>
      <li>
        <p><span>A player shall not:</span></p>
        <ol>
          <li>
            <span>deliberately cause delay in, or suspension of, play;</span>
          </li>
          <li>
            <span
              >deliberately modify or damage the shuttle in order to change its
              speed or its flight;</span
            >
          </li>
          <li><span>behave in an offensive manner; or</span></li>
          <li>
            <span
              >be guilty of misconduct not otherwise covered by the Laws of
              Badminton.</span
            >
          </li>
        </ol>
      </li>
      <li>
        <p><span>Administration of breach</span></p>
        <ol>
          <li>
            <p>
              <span
                >The umpire shall administer any breach of Law 16.4.1, 16.5.2 or
                16.6 by:</span
              >
            </p>
            <ol>
              <li><span>issuing a warning to the offending side; or</span></li>
              <li>
                <span
                  >faulting the offending side, if previously warned; or</span
                >
              </li>
              <li>
                <span
                  >faulting the offending side in cases of flagrant offence or
                  breach of Law 16.2.</span
                >
              </li>
            </ol>
          </li>
          <li>
            <p>
              <span
                >On faulting a side (Law 16.7.1.2 or 16.7.1.3), the umpire shall
                report the offending side immediately to the Referee, who shall
                have the power to disqualify the offending side from the
                match.</span
              >
            </p>
          </li>
        </ol>
      </li>
    </ol>
    <h2 id="17-OFFICIALS-AND-APPEALS" data-id="17-OFFICIALS-AND-APPEALS">
      <span>17. OFFICIALS AND APPEALS</span>
    </h2>
    <ol>
      <li>
        <p>
          <span
            >The Referee shall be in overall charge of the tournament or
            championship(s) of which a match forms part.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >The umpire, where appointed, shall be in charge of the match, the
            court and its immediate surrounds. The umpire shall report to the
            Referee.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >The service judge shall call service faults made by the server
            should they occur (Law 9.1.2 to 9.1. 9 ).</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >A line judge shall indicate whether a shuttle landed ‘in’ or ‘out’
            on the line(s) assigned.</span
          >
        </p>
      </li>
      <li>
        <p>
          <span
            >An official’s decision shall be final on all points of fact for
            which that official is responsible except that if,</span
          >
        </p>
        <ol>
          <li>
            <p>
              <span
                >in the opinion of the umpire, it is beyond reasonable doubt
                that a line judge has clearly made a wrong call, the umpire
                shall overrule the decision of the line judge.</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >an Instant Review System is in operation, the Referee shall
                decide on any line call challenge using the system (Appendix
                7).</span
              >
            </p>
          </li>
        </ol>
      </li>
      <li>
        <p><span>An umpire shall:</span></p>
        <ol>
          <li>
            <p>
              <span
                >uphold and enforce the Laws of Badminton and, especially, call
                a ‘fault’ or a ‘let’ should either occur;</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >give a decision on any appeal regarding a point of dispute, if
                made before the next service is delivered;</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >ensure players and spectators are kept informed of the progress
                of the match;</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >appoint or replace line judges or a service judge in
                consultation with the Referee;</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >where another technical official is not appointed, arrange for
                that official’s duties to be carried out;</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >where an appointed official is unsighted, carry out that
                official’s duties or play a ‘let’;</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >record and report to the Referee all matters relating to Law
                16; and</span
              >
            </p>
          </li>
          <li>
            <p>
              <span
                >refer to the Referee all unsatisfied appeals on questions of
                law only. (Such appeals must be made before the next service is
                delivered or, if at the end of the match, before the side that
                appeals has left the court.)</span
              >
            </p>
          </li>
        </ol>
      </li>
    </ol>
